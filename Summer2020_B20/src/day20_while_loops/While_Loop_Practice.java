package day20_while_loops;

public class While_Loop_Practice {
    public static void main(String[] args){
        for(int i = 1; i <= 10; i++){
            System.out.print(i + " ");
        }
        System.out.println();

        int num =1;
        while(num <= 5){
            System.out.print("Ryan");
            num++;
        }
    }
}
