package day19_for_loop;

public class Break_Statement {
    public static void main(String[] args){
        for(int i=0; i<5; i++){
            System.out.println("Hello");
            break;
        }
    for(int i = 10; i <= 50; i += 10){
        System.out.print(i + " ");
        if(i == 30){
            break;
        }
    }

    }
}
